/*函数参数为指针：指针可以改变指向，也可以改变指向内存的内容，如不想改变，需使用 const int* const p,较为繁琐*/
/*函数参数为引用：引用即别名，且绑定的对象中途不可以发生改变，如果不想改变绑定对象的值，使用 const int& p 即可*/
#include <iostream>
int main()
{
    int a = 10, b=15;
    int *p1 = &a;//普通指针

    /*常量指针*/
    const int * p2 = &a;
    (*p2) = 5;//错误：常量指针指向的值为常量，不可以更改指向内存的内容
    p2 = &b;//正确：可以更改指针指向的内存
    
    /*指针常量*/
    int* const p3 = &a;
    (*p3 ) = 20; //正确：可以改变指向内存的内容
    p3 = &b; //错误：不可以改变指向的内存

}